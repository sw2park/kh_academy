package operator;

public class Main02 {

	public static void main(String[] args) {
		// 나눗셈
		int num1 = 7;
		int num2 = 3;
		int result1 = num1 / num2;
		System.out.println("7 / 3 = " + result1);
		
		// 0으로 나누기
		int zero = 0;
		int result2 = num1 / zero;
		
		System.out.println("7 / 0 = " + result2);
		
	}

}
