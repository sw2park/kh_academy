package operator;

public class Main01 {

	public static void main(String[] args) {
		int num1 = 12;
		int num2 = 8; 
		
		int result1 = num1 + num2;
		
		System.out.println("12 + 8 = " + result1);
		result1 = num1-num2;
		System.out.println("12 - 8 = " + result1);
		result1 = num1*num2;
		System.out.println("12 * 8 = " + result1);
		result1 = num1/num2;
		System.out.println("12 / 8 = " + result1);
		result1 = num1%num2;
		System.out.println("12 % 8 = " + result1);
	}
}
